###
# 01 - print()
# El móddulo print() es el módulo que sirve para imprimir en la consola, es decir, mostrar un mensaje al usuario.
###
print("¡Hola, Mundo!")
print('También funciona con una sola comilla')

# Puedes imprimir varias cosas separadas por comas
print("Python", "es", "genial") 

# Puedes cambiar el separador entre los elementos
print("Python", "es", "brutal", sep="-") 

# Puedes cambiar el final de la línea, por defecto es un salto de línea
print("Esto se impreime", end=" ")
print("en la misma línea") 

